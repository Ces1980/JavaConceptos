
package com.practicascesar.proyecto1;

public class HolaMundo {

    public static void main(String[] args) {
        
        System.out.println("Tipos de datos....\t\n");
        
        byte mordida;
        mordida = 126;
        System.out.println("Mordida: "+ mordida);
         
        short corto;
        corto = 32000;
        System.out.println("Short: "+ corto);
        
        int entero;
        entero = 32000;
        System.out.println("Entero: " + entero);
        
        long largo;
        largo = 253265874;
        System.out.println("Largo: " + largo);
        
        float flotante;
        flotante = 526325.23f;
        System.out.println("Float: " + flotante);
        
        double doble;
        doble = 124585744512.25;
        System.out.println("Double: " + doble);
        
        char letra;
        letra = 'a';
        System.out.println("Letra: "+ letra);
        
        
        boolean bandera;
        bandera = false;
        System.out.println("Bandera: "+ bandera);
    }
 
    
    
}
